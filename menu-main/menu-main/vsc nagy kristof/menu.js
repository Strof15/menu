//Házhozszállítás
function rendelesGombClick() {
    var checkBox = document.getElementById("RendelesCheckbox");
    if (document.getElementById("RendelesIranyitoSzam").value != "" && document.getElementById("RendelesTelepules").value != "" && document.getElementById("RendelesUtca").value != "" && document.getElementById("RendelesHazszam").value != "" && document.getElementById("RendelesEmail").value != "" && document.getElementById("RendelesNev").value != "" && document.getElementById("RendelesIranyitoSzam").value != "" && checkBox.checked == true)
    {
        alert("Rendelés sikeres! Küldtünk egy visszaigazoló emailt a " + document.getElementById("RendelesEmail").value + " címre. Jó étvágyat!");
    }
}

